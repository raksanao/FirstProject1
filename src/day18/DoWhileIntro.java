package day18;

public class DoWhileIntro {
    public static void main(String[] args) {
        //count from 1 o 5
        int x = 1;
        do {
            System.out.println(x);
            ++x;
        } while (x < 5);
        System.out.println("____________");

        int y = 5;
        do{
        System.out.println(y);
        --y;
        } while (y>=0);






    }
}
